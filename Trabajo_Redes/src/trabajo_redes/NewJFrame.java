        if(jComboBox1.getSelectedItem()== "Fibra Optica")
        {
            c1 = (c1*0.02)/100;
        }
        if(jComboBox1.getSelectedItem()== "Cable UTP")
        {
            c1 = (c1*25)/100;
        }
        if(jComboBox2.getSelectedItem() == "Cable Coaxial")
        {
            c2 = (c2*20)/100;
        }
        if(jComboBox2.getSelectedItem() == "Fibra Optica")
        {
            c2 = (c2*0.02)/100;
        }
        if(jComboBox1.getSelectedItem() == "Cable UTP")
        {
            c2= (c2*25)/100;
        }
        d=sen+g1+g2+f+c1+c2;
